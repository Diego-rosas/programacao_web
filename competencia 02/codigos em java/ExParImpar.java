public class ExParImpar {
    public static void main(String[] args) {
        
        for (var i = 0; i < 10; i++) {
            if(i % 2 == 0) {
                System.out.println(i + "É PAR!!");
            } else {
                System.out.println(i + "É IMPAR!!");
            }
        }
    }
}
